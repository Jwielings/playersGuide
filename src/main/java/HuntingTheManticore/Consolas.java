package HuntingTheManticore;

public class Consolas {
   private int startHp;
   private int newHp;

    public Consolas(int startHp) {
        this.startHp = startHp;
    }

    public int getStartHp() {
        return startHp;
    }
    public void setStartHp(int startHp) {
        this.startHp = startHp;
    }

    public int getNewHp() {
        return newHp;
    }

    public void setNewHp(int newHp) {
        this.newHp = newHp;


    }
}
